-- 石垣市議会版 みらい議会
-- 第3回定例会 本文整備第9弾議案をトップのジャンル別表示に追加するSQL
--
-- タグ割り当て:
-- - 第5号  -> 予算・財政
-- - 第6号  -> 健康・医療・福祉
-- - 第7号  -> 健康・医療・福祉
-- - 第8号  -> 健康・医療・福祉
-- - 第9号  -> 自治・まちづくり
-- - 第10号 -> 予算・財政
-- - 第11号 -> 上下水道・インフラ
-- - 第12号 -> 上下水道・インフラ

begin;

update public.bills
set
  is_featured = false,
  updated_at = now()
where name in (
  '議案第5号 令和7年度石垣市一般会計補正予算（第12号）',
  '議案第6号 令和7年度石垣市国民健康保険事業特別会計補正予算（第5号）',
  '議案第7号 令和7年度石垣市後期高齢者医療特別会計補正予算（第3号）',
  '議案第8号 令和7年度石垣市介護保険事業特別会計補正予算（第4号）',
  '議案第9号 令和7年度石垣都市計画土地区画整理事業特別会計補正予算（第2号）',
  '議案第10号 令和7年度石垣市港湾事業特別会計補正予算（第5号）',
  '議案第11号 令和7年度石垣市下水道事業会計補正予算（第2号）',
  '議案第12号 令和7年度石垣市水道事業会計補正予算（第6号）'
);

insert into public.bills_tags (
  bill_id,
  tag_id
)
select
  b.id,
  t.id
from (
  values
    ('議案第5号 令和7年度石垣市一般会計補正予算（第12号）', '予算・財政'),
    ('議案第6号 令和7年度石垣市国民健康保険事業特別会計補正予算（第5号）', '健康・医療・福祉'),
    ('議案第7号 令和7年度石垣市後期高齢者医療特別会計補正予算（第3号）', '健康・医療・福祉'),
    ('議案第8号 令和7年度石垣市介護保険事業特別会計補正予算（第4号）', '健康・医療・福祉'),
    ('議案第9号 令和7年度石垣都市計画土地区画整理事業特別会計補正予算（第2号）', '自治・まちづくり'),
    ('議案第10号 令和7年度石垣市港湾事業特別会計補正予算（第5号）', '予算・財政'),
    ('議案第11号 令和7年度石垣市下水道事業会計補正予算（第2号）', '上下水道・インフラ'),
    ('議案第12号 令和7年度石垣市水道事業会計補正予算（第6号）', '上下水道・インフラ')
) as v(bill_name, tag_label)
join public.bills b
  on b.name = v.bill_name
join public.tags t
  on t.label = v.tag_label
on conflict (bill_id, tag_id) do nothing;

commit;

select
  b.name,
  b.is_featured,
  t.label as tag_label,
  t.featured_priority
from public.bills b
left join public.bills_tags bt
  on bt.bill_id = b.id
left join public.tags t
  on t.id = bt.tag_id
where b.name in (
  '議案第5号 令和7年度石垣市一般会計補正予算（第12号）',
  '議案第6号 令和7年度石垣市国民健康保険事業特別会計補正予算（第5号）',
  '議案第7号 令和7年度石垣市後期高齢者医療特別会計補正予算（第3号）',
  '議案第8号 令和7年度石垣市介護保険事業特別会計補正予算（第4号）',
  '議案第9号 令和7年度石垣都市計画土地区画整理事業特別会計補正予算（第2号）',
  '議案第10号 令和7年度石垣市港湾事業特別会計補正予算（第5号）',
  '議案第11号 令和7年度石垣市下水道事業会計補正予算（第2号）',
  '議案第12号 令和7年度石垣市水道事業会計補正予算（第6号）'
)
order by t.featured_priority nulls last, b.name, t.label;
